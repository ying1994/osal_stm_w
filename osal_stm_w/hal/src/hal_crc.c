/**
 * @brief 计算CRC16
 * @param uCRC16 CRC16的初始值
 * @param pData 输入数据指针
 * @param uLen 输入数据长度
 * @return UINT16 CRC校验结果
 */
UINT16 HalCRC16_8005(UINT16 uCRC16, UCHAR * pData, UINT16 Len) 
{
    UINT16 temp = 0;
	UINT16 i;
	for(i=0; i<Len; i++)
	{
		temp = uCRC16 & 0xff;
		uCRC16 = (uCRC16 >> 8) ^ crc16_table_8005[(temp ^ pData[i]) & 0xFF];
	}

  return uCRC16;  
}

/**
 * @brief 计算CRC16
 * @param uCRC16 CRC16的初始值
 * @param pData 输入数据指针
 * @param uLen 输入数据长度
 * @return UINT16 CRC校验结果
 */
UINT16 HalCRC16_CCITT(UINT16 uCRC16, UCHAR* pData, UINT16 uLen)
{
    UINT16 i = 0;
    UINT16 tabitem = 0;

    for (i = 0; i < uLen; i++)
    {
        tabitem=( uCRC16 >> 8 )^ pData[i];
        uCRC16 = ( uCRC16 << 8 ) ^ crc16_table_ccitt[tabitem];
    }
    return uCRC16;
}

