/**
 * @brief 计算CRC16
 * @param uCRC16 CRC16的初始值
 * @param pData 输入数据指针
 * @param uLen 输入数据长度
 * @return UINT16 CRC校验结果
 */
quint16 bd_crc_CRC16(quint16 uCRC16, quint8* pData, quint32 uLen)
{
    quint16 i = 0;
    quint16 tabitem = 0;

    for (i = 0; i < uLen; i++)
    {
        tabitem=( uCRC16 >> 8 )^ pData[i];
        uCRC16 = ( uCRC16 << 8 ) ^ crc16_table[tabitem];
    }
    return uCRC16;
}

/**
 * @brief 计算CRC32
 * @param uCRC16 CRC32的初始值
 * @param pData 输入数据指针
 * @param uLen 输入数据长度
 * @return UINT32 CRC校验结果
 */
quint32 bd_crc_CRC32(quint32 uCRC32, quint8* pData, quint32 uLen)
{
    quint32 i = 0;
    quint32 tabitem = 0;

    for (i = 0; i < uLen; i++)
    {
        tabitem=( uCRC32 >> 24 )^ pData[i];
        uCRC32 = ( uCRC32 << 8 ) ^ crc32_table[tabitem];
    }
    return uCRC32;
}

